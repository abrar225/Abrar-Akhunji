/**
 * gen-php-config.mjs — runs after `vite build`.
 *
 * Writes dist/api/config.php with the FreeLLMAPI secret pulled from .env (or the
 * real environment). The key therefore lives ONLY in this server-side PHP file,
 * never in the client bundle and never in source control.
 *
 * Recognised keys (from .env or process.env):
 *   FREELLM_API_KEY   (preferred, server-only)  or  VITE_FREELLM_API_KEY
 *   FREELLM_URL / VITE_FREELLM_URL      default https://freellm-for-fixo.vercel.app/v1
 *   FREELLM_MODEL / VITE_FREELLM_MODEL  default auto
 *   TWIN_DAILY_LIMIT                    default 10
 *   TWIN_ALLOW_ORIGINS (comma list)    default [] (any)
 */
import { readFileSync, existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

function loadEnv() {
  const env = {};
  for (const f of ['.env', '.env.local', '.env.production', '.env.production.local']) {
    const p = resolve(root, f);
    if (!existsSync(p)) continue;
    for (const line of readFileSync(p, 'utf8').split('\n')) {
      const m = line.match(/^\s*([A-Za-z0-9_]+)\s*=\s*(.*?)\s*$/);
      if (m && !line.trim().startsWith('#')) env[m[1]] = m[2].replace(/^["']|["']$/g, '');
    }
  }
  return { ...env, ...process.env };
}

const env = loadEnv();
const key = env.FREELLM_API_KEY || env.VITE_FREELLM_API_KEY || '';
const base = env.FREELLM_URL || env.VITE_FREELLM_URL || 'https://freellm-for-fixo.vercel.app/v1';
const model = env.FREELLM_MODEL || env.VITE_FREELLM_MODEL || 'auto';
const limit = parseInt(env.TWIN_DAILY_LIMIT || '10', 10) || 10;
const origins = (env.TWIN_ALLOW_ORIGINS || '')
  .split(',')
  .map((s) => s.trim())
  .filter(Boolean);

const esc = (s) => String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
const originsPhp = origins.map((o) => `'${esc(o)}'`).join(', ');

const php = `<?php
// AUTO-GENERATED by scripts/gen-php-config.mjs — do NOT commit, do NOT edit by hand.
// Prefers a real environment variable (set one in hPanel for max safety); the
// literal below is the fallback baked in at build time.
return [
    'api_key'       => getenv('FREELLM_API_KEY') ?: '${esc(key)}',
    'base_url'      => '${esc(base)}',
    'model'         => '${esc(model)}',
    'daily_limit'   => ${limit},
    'allow_origins' => [${originsPhp}],
];
`;

const outDir = resolve(root, 'dist', 'api');
mkdirSync(outDir, { recursive: true });
writeFileSync(resolve(outDir, 'config.php'), php, 'utf8');

if (!key) {
  console.warn(
    '\n\x1b[33m[gen-php-config] ⚠  No API key found in .env — dist/api/config.php was written WITHOUT a key.\n' +
      '   Set FREELLM_API_KEY in .env (or in Hostinger hPanel env) before deploying.\x1b[0m\n'
  );
} else {
  console.log('\x1b[32m[gen-php-config] ✓ dist/api/config.php written (key kept server-side).\x1b[0m');
}
